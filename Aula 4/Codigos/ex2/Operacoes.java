/**
 * Classe abstrata que representa as operações
 *
 */
public abstract class Operacoes {
	/**
	 * Método que calculará as operações para cada classe
	 * @param operando1 operando 1 da operação
	 * @param operando2 operando 2 da operação
	 * @return resultado da operação
	 */
	public abstract float calculaOperacao(float operando1, float operando2);
}
